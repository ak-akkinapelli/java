public class FizzBuzz {
    public static void main(String [] args)
    {  
    {   
        for(int x =1;x<=100;x++)
        {
            
            if(x%3==0)
            {
            System.out.println("fizz");
            }
            else if(x%5==0){
                System.out.println(("buzz"));
            }
            System.out.println(x);

        }
}
}
}
